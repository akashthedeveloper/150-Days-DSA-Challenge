// Write a program to print the product of 2 numbers

#include <iostream>
using namespace std;

int main() {
    int num1,num2;
    cout << "Enter the first number:" << endl;
    cin >> num1;
    cout << "Enter the second number:" << endl;
    cin >> num2;
    int product = num1 * num2;
    cout << "Product of two numbers is :" << product << endl;
    return 0;
}
