// Relational Operator

#include <iostream>
using namespace std;

int main(){

    int a =3;
    int b =6;

    bool n1 = (a==b);
    cout << n1 <<endl;

    bool n2 = (a>=b);
    cout << n2 <<endl;

    bool n3 = (a<=b);
    cout << n3 <<endl;

    bool n4 = (a>b);
    cout << n4 <<endl;

    bool n5 = (a<b);
    cout << n5 <<endl;

    bool n6 = (a!=b);
    cout << n6 <<endl;

    return 0;
}